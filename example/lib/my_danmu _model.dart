import 'package:fanjiao_danmu/fanjiao_danmu/fanjiao_danmu.dart';
import 'package:flutter/material.dart';

class MyDanmuModel extends DanmuModel {
  int likeCount;
  bool isLiked;

  MyDanmuModel({
    this.likeCount = 0,
    this.isLiked = false,
    required int id,
    required String text,
    required Duration startTime,
    List<InlineSpan> spans = const [],
    ImageProvider? imageProvider,
    String? package,
    bool isRepeatable = false,
    double opacity = 1,
    double? specifyY,
    EdgeInsets padding = const EdgeInsets.symmetric(vertical: 2, horizontal: 8),
    EdgeInsets margin = const EdgeInsets.symmetric(vertical: 2, horizontal: 8),
    double strokeWidth = 1,
    Size imageSize = const Size(52, 20),
    int flag = DanmuFlag.scroll | DanmuFlag.clickable,
    BoxDecoration? decoration,
    BoxDecoration? foregroundDecoration,
    AlignmentGeometry? alignment,
    Duration? insertTime,
    TextStyle textStyle = const TextStyle(
      color: Colors.white,
      fontSize: 18,
      fontWeight: FontWeight.w500,
      decoration: TextDecoration.none,
    ),
  }) : super(
          id: id,
          text: text,
          startTime: startTime,
          spans: spans,
          specifyY: specifyY,
          imageProvider: imageProvider,
          package: package,
          isRepeatable: isRepeatable,
          opacity: opacity,
          padding: padding,
          margin: margin,
          strokeWidth: strokeWidth,
          imageSize: imageSize,
          flag: flag,
          decoration: decoration,
          foregroundDecoration: foregroundDecoration,
          alignment: alignment,
          insertTime: insertTime,
          textStyle: textStyle,
        );

  @override
  MyDanmuModel copyWith(
      {bool? isLiked,
      int? likeCount,
      int? id,
      String? text,
      double? specifyY,
      Duration? startTime,
      List<InlineSpan>? spans,
      bool? isClickable,
      ImageProvider<Object>? imageProvider,
      String? package,
      bool? isRepeatable,
      double? opacity,
      EdgeInsets? padding,
      EdgeInsets? margin,
      double? strokeWidth,
      Size? imageSize,
      int? flag,
      BoxDecoration? decoration,
      BoxDecoration? foregroundDecoration,
      AlignmentGeometry? alignment,
      Duration? insertTime,
      TextStyle? textStyle}) {
    return MyDanmuModel(
      isLiked: isLiked ?? this.isLiked,
      likeCount: likeCount ?? this.likeCount,
      id: id ?? this.id,
      text: text ?? this.text,
      specifyY: specifyY ?? this.specifyY,
      startTime: startTime ?? this.startTime,
      spans: spans ?? this.spans,
      imageProvider: imageProvider ?? this.imageProvider,
      package: package ?? this.package,
      isRepeatable: isRepeatable ?? this.isRepeatable,
      opacity: opacity ?? this.opacity,
      padding: padding ?? this.padding,
      margin: margin ?? this.margin,
      strokeWidth: strokeWidth ?? this.strokeWidth,
      imageSize: imageSize ?? this.imageSize,
      flag: flag ?? this.flag,
      decoration: decoration ?? this.decoration,
      foregroundDecoration: foregroundDecoration ?? this.foregroundDecoration,
      alignment: alignment ?? this.alignment,
      insertTime: insertTime ?? this.insertTime,
      textStyle: textStyle ?? this.textStyle,
    );
  }
}
